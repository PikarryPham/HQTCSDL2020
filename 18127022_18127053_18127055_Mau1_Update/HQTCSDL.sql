CREATE TABLE NHANVIEN (
	MA_NV CHAR(5) UNIQUE NOT NULL,
	TEN_NV NVARCHAR(50) NOT NULL,
	SDT VARCHAR(15) NOT NULL,
	DCHI_NV NVARCHAR(250) DEFAULT NULL,
	NGSINH DATE DEFAULT NULL,
	LUONG FLOAT DEFAULT NULL,
	CHECK (LUONG > 0),
	USER_NV VARCHAR(50) UNIQUE NOT NULL,
	PASS_NV VARCHAR(50) NOT NULL,
	LAMVIEC CHAR(5)

	CONSTRAINT PK_NHANVIEN -- dat ten khoa chinh
	PRIMARY KEY (MA_NV)
);
GO

CREATE TABLE CHINHANH (
	MA_CN CHAR(5) UNIQUE NOT NULL,
	SDT_CN VARCHAR(15) NOT NULL,
	FAX_CN VARCHAR(15) NOT NULL,
	NVQUANLY CHAR(5) DEFAULT NULL,
	
	CONSTRAINT PK_CHINHANH -- dat ten khoa chinh
	PRIMARY KEY (MA_CN)
);
GO

CREATE TABLE CHINHANH_DCHI(
	MA_CN CHAR(5) UNIQUE NOT NULL,
	KHUVUC NVARCHAR(30),
	DUONG NVARCHAR(30)  NOT NULL,
	QUAN NVARCHAR(30)  NOT NULL,
	TPHO NVARCHAR(40)  NOT NULL

	CONSTRAINT PK_CHINHANH_DCHI
	PRIMARY KEY (MA_CN)
);
GO

CREATE TABLE NHA
(
	MA_NHA CHAR(5) UNIQUE NOT NULL,
	NGAYDANG DATE  NOT NULL,
	NGAYHETHAN DATE,
	CHECK (NGAYHETHAN >= NGAYDANG),
	SLPHONG INTEGER  NOT NULL,
	CHECK (SLPHONG > 0),
	TINHTRANG NVARCHAR(100)  NOT NULL,
	VIEW_NHA INTEGER  NOT NULL,
	TRANGTHAI BIT NOT NULL,
	NVPT CHAR(5),
	MA_CN CHAR(5),
	MALN CHAR(5),
	MA_CNHA CHAR(5)

	CONSTRAINT PK_NHA
	PRIMARY KEY (MA_NHA)
);
GO

CREATE TABLE NHABAN
(
	MA_NHA CHAR(5) UNIQUE NOT NULL,
	GIABAN INTEGER NOT NULL,
	CHECK (GIABAN >= 100000),
	DKCHUNHA NVARCHAR(50)

	CONSTRAINT PK_NHABAN
	PRIMARY KEY (MA_NHA)

); 
