CREATE TABLE KHACHHANG
(
	MA_KH CHAR(5) UNIQUE NOT NULL,
	TEN_KH NVARCHAR(50) NOT NULL,
	SDT VARCHAR(15) NOT NULL,
	TIEUCHI NVARCHAR(100) DEFAULT NULL,
