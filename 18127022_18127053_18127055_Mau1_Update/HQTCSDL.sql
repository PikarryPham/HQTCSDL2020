CREATE TABLE HOPDONG (
	MA_HD CHAR(5) UNIQUE NOT NULL,
	TRANGTHAI_HD CHAR(3) NOT NULL,
	NDCV NVARCHAR(100) NOT NULL,
	NGAYBD_HD DATE NOT NULL,
	NGAYKT_HD DATE DEFAULT NULL,
	CHECK (NGAYKT_HD >= NGAYBD_HD),
	MA_CNHA CHAR(5),
