CREATE TABLE CHUNHA (
	 MA_CNHA CHAR(5) UNIQUE NOT NULL,
	 TEN_CNHA NVARCHAR(50) NOT NULL,
	 DCHI_CNHA NVARCHAR(250) DEFAULT NULL,
	 SDT VARCHAR(15) NOT NULL,
